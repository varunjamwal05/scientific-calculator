# All The Trignometry Functions Of Calculator
import math

def sine(num):
    return math.sin(num)
def cosine(num):
    return math.cos(num)
def tangent(num):
    return math.tan(num)
def arcsine(num):
    return math.asin(num)
def arccosine(num):
    return math.acos(num)
def arctangent(num):
    return math.atan(num)